//Use Basic Operators
// Arithmetic Operators
var a = 5;
var b = 10;

console.log("Addition: ", a + b);//15
console.log("Subtraction: ", b - a);//5
console.log("Multiplication: ", a * b);//50
console.log("Division: ", b / a);//2
console.log("Modulus: ", b % a);//0

//Comparison Operators
console.log("Equal to: ", a == b); //false
console.log("Not equal to: ", a != b); //true
console.log("Greater than: ", b > a); //true
console.log("Less than: ", a < b);//true
console.log("Greater than or equal to: ", b >= a);//true
console.log("Less than or equal to: ", a <= b);//true

//Logical Operators
var c = true;
var d = false;
console.log("AND (c && d): ", c && d); //false
console.log("OR (c || d): ", c || d); //true
console.log("NOT (!c): ", !c); //false

